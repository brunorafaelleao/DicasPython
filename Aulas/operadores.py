"""
Treinamento de operadores em Python
"""
# inserir o valor de a
a = int(input("Digite um valor para X: "))
#inserir o valor de b
b = int(input("Digite um valor para Y: "))

#verficando que é o maior:
if a > b:
    print(f"O maior valor é o de X: {a}")
elif a < b:
    print(f"O maior valor é o de Y: {b}")
else:
    print("Os valores são iguais")


